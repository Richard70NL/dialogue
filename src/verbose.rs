/************************************************************************************************/

pub struct Verbose {
    enabled: bool,
    indent: usize,
}

/************************************************************************************************/

impl Verbose {
    /*------------------------------------------------------------------------------------------*/

    pub fn new() -> Verbose {
        Verbose {
            enabled: false,
            indent: 0,
        }
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn enable(&mut self) {
        self.enabled = true;
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn _disable(&mut self) {
        self.enabled = false;
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn _is_enabled(&self) -> bool {
        self.enabled
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn increate_indent(&mut self) {
        self.indent += 1;
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn decrease_indent(&mut self) {
        if self.indent > 0 {
            self.indent -= 1;
        }
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn println(&self, line: &str) {
        if self.enabled {
            let indent = "  ".repeat(self.indent);
            println!("{}{}", indent, line);
        }
    }

    /*------------------------------------------------------------------------------------------*/
}

