/************************************************************************************************/

pub struct Group {
    group_id: String,
    article_count: i32,
    low_water_mark: i32,
    high_water_mark: i32,
}

/************************************************************************************************/

impl Group {
    /*------------------------------------------------------------------------------------------*/

    pub fn new(
        group_id: String,
        article_count: i32,
        low_water_mark: i32,
        high_water_mark: i32,
    ) -> Group {
        Group {
            group_id,
            article_count,
            low_water_mark,
            high_water_mark,
        }
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn get_group_id(&self) -> &String {
        &self.group_id
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn get_article_count(&self) -> i32 {
        self.article_count
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn get_low_water_mark(&self) -> i32 {
        self.low_water_mark
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn get_high_water_mark(&self) -> i32 {
        self.high_water_mark
    }

    /*------------------------------------------------------------------------------------------*/
}

/************************************************************************************************/
